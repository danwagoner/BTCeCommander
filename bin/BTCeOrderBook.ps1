					$dgv_ask.Item(1,$x).Style.BackColor = $wallcolor
					$dgv_ask.Item(2,$x).Style.BackColor = $wallcolor
				}
			}
			
			# Buy wall visual changes
			for($x=0;$x -le $dgv_bid.RowCount-1;$x++){
				$currentcellval = $dgv_bid.Item(1,$x).Value
