	function GetStringHash([String] $String) { 
		
		$hmacsha = New-Object System.Security.Cryptography.HMACSHA512
		$hmacsha.key = [Text.Encoding]::ASCII.GetBytes($apisecret)
		$signature = $hmacsha.ComputeHash([Text.Encoding]::ASCII.GetBytes($String))
